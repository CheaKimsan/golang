package main

import "fmt"

func main() {
	// variable of type uint8
	var a uint8
	a = 10
	fmt.Println(a)

	// variable of type in t8
	var b int = 200000
	fmt.Println(b)

	// Byte
	var myByte byte = 'A'
	fmt.Println(myByte)

	// Rune type
	var myRune rune = 'B'
	fmt.Println(myRune)

	var name string = "Chea Kimsan"
	var year int = 3
	var cause string = "MIS"
	var dob string = "2003-09-07"

	fmt.Println(name)
	fmt.Println(year)
	fmt.Println(cause)
	fmt.Println(dob)

}
