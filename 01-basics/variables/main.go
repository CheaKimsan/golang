package main

import "fmt"

func main() {
	// string no vlaue is = ""
	var myString string
	fmt.Println(myString)

	myString = "Welcome to GDI"
	fmt.Println(myString)

	// int no vlaue is = 0
	var myInt int
	fmt.Println(myInt)

	myInt = 10
	fmt.Println(myInt)

	// boolean default value is false
	var myBoolean bool
	fmt.Println(myBoolean)

	myBoolean = true
	fmt.Println(myBoolean)

}
